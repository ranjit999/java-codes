class SwitchDemo{
	
	public static void main(String... args){
		int a = 100;
		switch(a){

			case 10: System.out.println("10");
			case 20: System.out.println("20");break;
			default: System.out.println("default");
			case 30: System.out.println("30");
			case 40: System.out.println("40");
			
		}
	}
	
}